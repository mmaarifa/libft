int	ft_atoi(const char *str)
{
	int	i;
	int	bb;

	bb = 0;
	i = 0;
	if (str[0] == '-' || str[0] == '+' || str [0] == ' '
		|| (str[0] >= 9 && str[0] <= 13))
		i++;
	else if (str[i] == '-' || str[i] == '+' || str[i] == ' '
		|| (str[i] >= 9 && str[i] <= 13))
		return (0);
	while (str[i] >= '0' && str[i] <= '9')
	{
		bb = bb * 10 + str[i] - '0';
		i++;
	}
	if (str[0] == '-')
		return (bb * -1);
	else
		return (bb);
}

char	*ft_strrchr(const char *s, int c)
{
	int i;
	
	i = 0;
	while (s[i])
		i++;
	while (s[i-1])
	{
		if (c == s[i])
			return ((char*)&s[i]);
		i--;
	}
	return ("NULL");
}
